
#인덱싱[이상 : 미만 : 간격]
print("안녕하세요"[0])
print("안녕하세요"[1])
print("안녕하세요"[2])     #인덱싱
print("안녕하세요"[0:4])   #슬라이싱 범위 중 뒤 숫자 포함 안함 중요
print("안녕하세요"[3:])    #뒤에 숫자 안넣으면 끝까지
print("안녕하세요"[-1])
print("안녕하세요"[-2])
print("안녕하세요"[1:5:2]) 

print(len("안녕하세요"))   #문자 길이
